                        <h3>Munshi Abdur Rouf (Bengali: 1 May 1943 – 1971) was a Lance Nayek in East Pakistan Rifles during the Bangladesh Liberation War. He enlisted in the East Bengal Regiment on 8 May 1963, and was attached with a regular infantry unit during War of Liberation. Munshi Abdur Rouf embraced martyrdom on 18 April 1971 at Burighat in Chittagong Hill Tracts after causing extensive damage to Pakistan Army with his MG and forcing them to retreat. He was buried at Naniarchor Upazilla in Rangamati District.


He was awarded Bir Sreshtho, which is the highest recognition of bravery in Bangladesh.


Early life


Munshi Abdur Rouf was born on 1 May 1943 at Salamatpur village under Boalmari thana (currently Madhukhali thana) in Faridpur District. His father Munshi Mehedi Hossain was an “Imam” at a local mosque and his mother was Mukidunnesa. After his father's death in 1955, Rouf had to stop his education at his eighth grade. He joined the East Pakistan Rifles on 8 May 1963. He had to increase his age three years in order to get the job. After the preliminary training at the EPR camp at Chuadanga, Rouf went to West Pakistan to receive advanced training. He was appointed to Comilla after 6 months.


Death


East Bengal Regiment wanted to restrict the Pakistan Army from using the Rangamati-Mahalchari waterway. Thus, they camped at both of the Chingri Lakes at Burighat. Munshi Abdur Rouf was serving as a soldier in this company.


In order to prevent the Pakistan Army from utilizing the Rangamati-Mahalchari waterway, 8th East Bengal Regiment constructed a camp at both sides

On 18 April 1971, Pakistan Army attacked at the defense position of Mukti Bahini with 7 speed boats and 2 launches. Their mission was to drive the Mukti Bahini away from the waterway of Rangamati and Mohalchari. Pakistani forces managed to disorient Mukti Bahini by coming closer to them and firing heavily. In the meantime Pakistanis surrounded the freedom fighters and managed to isolate nearly 100 of them. Rouf realized the threat to the entire company. So, he crawled forward to his trench and continuously fired towards the enemies with his automatic machine gun. As a result, Pakistanis dragged their launches back to a safer place and resumed their firing from there. Suddenly a mortar directly hit Rouf and he died immediately. Munshi Abdur Rouf's valiant effort helped his company to survive as his act saved nearly 150 soldiers of the Mukti Bahini on that day.</h3>
