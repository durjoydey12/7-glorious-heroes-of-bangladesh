                        <h3>At the start of Bangladesh Liberation War, Amin immediately resigned from Pakistan Navy and left “PNS Comilla” at Chittagong and came back to his village. He organized local youths and soldiers for the war. At May, he along with 500 others joined at Sector-3 under Major K M Shafiullah. Later he enrolled at “Palash”, a warship of Bangladesh Navy and at the same time worked as the Squadron leader for both “Padma” and “Palash”.


On 10 December an accident occurred at the Rupsha river in Khulna. Two of the warships of the Mukti Bahini named "Padma" and "Palash" mistakenly came under fire from the Indian Air Force as it crossed the river Rupsha near the Khulna shipyards. on this day. Earlier that day, Indian Air Force fired at different places of Khunla city. Incidentally one of those bombs had fallen at the warship "Palash". Ruhul Amin was working as an artificer of that ship. After the heavy bombing, the engine room got burned and Ruhul Amin was forced to dive into the water. When he reached the river shore, he got caught by the Razakars who killed him using their bayonets.</h3>
