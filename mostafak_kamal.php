                        <h3>Mostofa Kamal better known as Shaheed Sipahi Mostafa Kamal was a sepoy in the Bangladesh Army during the Liberation War.He was born on 16 December 1947 in Hajipur village of Daulatikhan upazilla under Bhola district. His father, Habibur Rahman was a Havilder.On 18 April 1971, Mostofa Kamal was killed in a defensive battle against the occupying Pakistan Army in Daruin village of Comilla. His actions inflicted heavy casualties on his enemy troops.He was awarded the highest recognition of bravery of the Bangladesh Army, Bir Sreshtho.


Mostafa Kamal was a Sepoy of the 4th East Bengal Regiment. At the start of March, 4th Regiment was moved from Moynamati Cantonment to Brahmanbaria. After the incidents of 25 March, 4th Regiment successfully brought Brahmanbaria under control. Under the leadership of Major Shafayat Jamil, they revolted against the Pakistani officers and formed a Defence Camp at Brahmanbaria. 4th Regiment continued their battle against the Pakistan Army and eventually arrived at Aakhaura. At Aakhaura, the 4th Regiment set their camp in Gangashagar and Talshahar. To avoid any unexpected attacked from the Pakistanis, 2nd Platoon of the company "Alpha" was sent to Daruin village. Mostafa Kamal was one of the section commanders of 2nd Platoon.


On 16 April, Pakistan Army started to approach towards the Regiment camp. They were coming over the railway of the Comilla-Aakhaura root. Regiment also reconciled their position at Daruin. They settled on their trenches near a pond. Mostafa Kamal took the rightmost position.


On 17 April, Pakistanis opened fire. Major Shafayat Jamil strengthened his force by sending the 11th Platoon of company Delta to Daruin. At around 12 PM the Pakistanis attacked from West. Another part of the Pakistan Army attacked Mukti Bahini from behind. As a result, Regiment had decided to retreat from Daruin. Mostafa Kamal took the responsibly to cover for his fellow fighters. Most of the fighters retreated to a safer position as Kamal relentlessly kept firing with his Light Machine Gun. He managed to neutralize(kill) 8 Pakistani soldiers. But at one stage, he got shot. He along with 20 to 40 other soldiers died at this battle.</h3>
